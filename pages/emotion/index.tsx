import { Anchor, Breadcrumbs, Text, Card, Center, Container, Flex, Group, Pagination, ScrollArea, Table } from '@mantine/core'
import { Emotion } from '@prisma/client'
import axios from 'axios'
import Head from 'next/head'
import React, { useEffect, useState } from 'react'
import DashboardLayout from '../../components/layout/DashboardLayout'

type Props = {}

const EmotionList = (props: Props) => {
  const items = [
    { title: 'Home', href: '/' },
    { title: 'Emotion List', href: '/emotions' },
  ].map((item, index) => (
    <Anchor href={item.href} key={index}>
      {item.title}
    </Anchor>
  ));

  const ths = (
    <tr>
      <th>Id</th>
      <th>User Id</th>
      <th>Name</th>
      <th>Description</th>
      <th>Latitude</th>
      <th>Longtitude</th>
      <th>Created At</th>
      <th>Updated At</th>
    </tr>
  );

  const [emotions, setEmotions] = useState([]);
  const [activePage, setPage] = useState(1);
  const [pageCount, setPageCount] = useState(1);

  const getData = async () => {
    const { data } = await axios.get("/api/emotions", {
      params: {
        page: activePage,
        limit: 10,
      }
    });
    setEmotions(data.data);

    setPageCount(data.pagination.pageCount)
  }

  useEffect(() => {
    getData()
  }, [activePage])

  const rows = emotions.map((element: Emotion) => (
    <tr key={element.id}>
      <td>{element.userId}</td>
      <td>{element.name}</td>
      <td>{element.description}</td>
      <td>{element.latitude}</td>
      <td>{element.longtitude}</td>
      <td>{new Date(element.createdAt).toLocaleDateString()}</td>
      <td>{new Date(element.updatedAt).toLocaleDateString()}</td>
    </tr>
  ));


  return (
    <>
      <Head>
        <title>Mutluluk Endeksi | Emotion List</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <DashboardLayout>
          <Container fluid h="50px" pb="20px">
            <Flex
              mih={50}
              gap="md"
              justify="flex-start"
              align="center"
              direction="row"
              wrap="wrap"
            >
              <Breadcrumbs separator="â†’">{items}</Breadcrumbs>
            </Flex>
          </Container>
          <Card withBorder shadow="sm" radius="md">
            <Card.Section withBorder inheritPadding py="xs">
              <Group position="apart">
                <Text weight={500}>Emotion List</Text>
              </Group>
            </Card.Section>
            <Card.Section mt="sm">
              <ScrollArea style={{ width: '100%' }}>
                <Table striped highlightOnHover withBorder withColumnBorders>
                  <caption>Emotion List</caption>
                  <thead>{ths}</thead>
                  <tbody>{rows}</tbody>
                  <tfoot>{ths}</tfoot>
                </Table>
              </ScrollArea>
            </Card.Section>
            <Card.Section>
              <Center w="100%" p="30px">
                <Pagination page={activePage} onChange={setPage} total={pageCount}></Pagination>
              </Center>
            </Card.Section>
          </Card>
        </DashboardLayout>
      </main>
    </>
  )
}

export default EmotionList